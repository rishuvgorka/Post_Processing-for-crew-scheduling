// to convert minutes into hours(HH:MM format)
string mintohour(int minutes)
{
    int hours = minutes / 60;
    int min = minutes % 60;

    string result;

    if (hours < 10)
    {
        if (min < 10)
        {
            result = "0" + to_string(hours) + ":0" + to_string(min);
        }
        else
        {
            result = "0" + to_string(hours) + ":" + to_string(min);
        }
    }
    else
    {
        if (min < 10)
        {
            result = to_string(hours) + ":0" + to_string(min);
        }
        else
        {
            result = to_string(hours) + ":" + to_string(min);
        }
    }

    return result;
}

// to convert hours(HH:MM) into minutes
int hourtomin(string hour)
{
    std::istringstream iss(hour);
    char colon;
    int hours, minutes;

    if (!(iss >> hours >> colon >> minutes) || colon != ':')
    {
        std::cerr << "Invalid input format: " << hour << std::endl;
        return -1;
    }

    int totalMinutes = hours * 60 + minutes;

    return totalMinutes;
}

//calcultes rest time between nth duty's last task and (n+1)th duty's first task
int rest(vector<string> a, vector<string> b)
{
    int day1 = 0, day2 = 0, ans = 0;
    if (a[0] == "M")
    {
        day1 = 1;
    }
    if (a[0] == "Tu")
    {
        day1 = 2;
    }
    if (a[0] == "W")
    {
        day1 = 3;
    }
    if (a[0] == "Th")
    {
        day1 = 4;
    }
    if (a[0] == "F")
    {
        day1 = 5;
    }
    if (a[0] == "Sa")
    {
        day1 = 6;
    }
    if (a[0] == "Su")
    {
        day1 = 7;
    }
    if (b[0] == "M")
    {
        day2 = 8;
    }
    if (b[0] == "Tu")
    {
        day2 = 9;
    }
    if (b[0] == "W")
    {
        day2 = 10;
    }
    if (b[0] == "Th")
    {
        day2 = 11;
    }

    if (b[0] == "F")
    {
        day2 = 12;
    }

    if (b[0] == "Sa")
    {
        day2 = 13;
    }

    if (b[0] == "Su")
    {
        day2 = 14;
    }

    if (hourtomin(a[3]) < hourtomin(a[7]))
    {
        ans = (day2 - day1 - 1) * 24 * 60 + 24 * 60 - hourtomin(a[7]) + hourtomin(b[3]);
    }
    else
    {
        ans = (day2 - day1 - 2) * 24 * 60 + 24 * 60 - hourtomin(a[7]) + hourtomin(b[3]);
    }
    return ans;
}

vector<vector<string>> sorting_and_statistical_calc(vector<vector<string>> arr, vector<vector<int>> arr2)
{
    vector<vector<string>> ans;  // to store the task details after sorting
    vector<vector<string>> summary;  // to store the statistical data like total NDH, total rest time, total duty time throughout a duty
    vector<string> first;
    int count = 1, last = 0;
    summary.push_back({"Duty no.", " Total Duty hours", " Total Rest hours", " Total NDH"});
    for (int i = 0; i < arr2.size(); i++)
    {
        int start = count;
        for (int j = 0; j < arr2[i].size() - 1; j++)
        {
            int rest_time = 0, duty_time = 0, night_hours = 0;
            vector<string> tofind = {"Duty", to_string(arr2[i][j]), "-"};
            int iter = compare(arr, tofind);
            tofind = {"Duty", to_string(arr2[i][j] + 1), "-"};
            if (j == 0)
            {
                first = arr[iter + 1];
            }

            string duty = "Duty " + to_string(count) + " -";
            ans.push_back({duty});
            int m = 1;
            while ((arr[iter + 1] != tofind) && (iter + 1 < arr.size()))
            {

                ans.push_back(arr[iter + 1]);
                if (j != 0 & m == 1)
                {
                    last = rest(ans[ans.size() - 3], ans[ans.size() - 1]);
                    ans[ans.size() - 3].push_back(mintohour(last));
                    summary[summary.size() - 1][2] = mintohour(hourtomin(summary[summary.size() - 1][2]) + last);
                    m--;
                }
                duty_time += hourtomin(arr[iter + 1][9]);
                night_hours += hourtomin(arr[iter + 1][10]);
                if (arr[iter + 1].size() > 11)
                {
                    rest_time += hourtomin(arr[iter + 1][11]);
                }
                iter++;
            }

            summary.push_back({duty.substr(0, duty.length() - 2), mintohour(duty_time), mintohour(rest_time), mintohour(night_hours)});

            count++;
        }
        last = rest(ans[ans.size() - 1], first);
        ans[ans.size() - 1].push_back(mintohour(last));
        summary[summary.size() - 1][2] = mintohour(hourtomin(summary[summary.size() - 1][2]) + last);
        ans.push_back({"After working duty number " + to_string(count - 1) + " the crew will pick up duty number " + to_string(start)});
    }
    ans.push_back({" ", "Summary"});
    for (int i = 0; i < summary.size(); i++)
    {
        ans.push_back(summary[i]);
    }
    return ans;
}

// 2-D vector generation of data
vector<vector<string>> strTOvec(string &data)
{
    vector<vector<string>> vecOfStrings;

    istringstream iss(data);
    string line;
    while (getline(iss, line))
    {
        istringstream lineStream(line);
        string word;
        vector<string> row;
        while (lineStream >> word)
        {
            row.push_back(word);
        }
        if (row[0] != "Duty" && row.size()>11)
        {
            row.erase(row.begin());
        }

        vecOfStrings.push_back(row);
    }

    return vecOfStrings;
}

